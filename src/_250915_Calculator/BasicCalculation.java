package _250915_Calculator;

public class BasicCalculation {
    private int a;
    private int b;

    public int divide(int a, int b) {
        return a / b;
    }

    public static int sum(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int multiple(int a, int b) {
        return a * b;
    }
}
